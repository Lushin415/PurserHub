    app.add_handler(
        CallbackQueryHandler(
            show_settings_menu,
            pattern=f"^{SettingsCB.SETTINGS_MENU}$|^settings$"
        )
    )
    section_pattern = "^(" + "|".join([
        SettingsCB.HELP_AUTH,
        SettingsCB.HELP_REALTY,
        SettingsCB.HELP_PVZ,
        SettingsCB.HELP_BLACKLIST,
        SettingsCB.HELP_SUBSCRIPTION,
        SettingsCB.HELP_CONTACT,
    ]) + ")$"
    app.add_handler(CallbackQueryHandler(show_help_section, pattern=section_pattern))
